/**
 * _isupper - check if a character from a to z is upper or lower
 * @c: store the elements that are going to be checked
 * Return: the function return 0 if the character is lowercase else return 1
 */

int _isupper(int c)
{
	
	if (c >= 'A' && c <= 'Z')
	{
		return (1);
	}

	else

	return (0);
}
